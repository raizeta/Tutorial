1. ng generate component NamaKomponen  || ng g c NamaKomponen
    Contoh : ng generate component hero-detail
	Perintah di Atas Akan Menggenerate Component Yang Akan Ditempatkan Di Folder src/app/
	Folder Yang Digenerate Namanya hero-detail yang berisi file sebagai berikut
	1. hero-detail.component.css
	2. hero-detail.component.html
	3. hero-detail.component.ts
	4. hero-detail.component.spec.ts

2. ng generate component NamaFolder/NamaKomponen  || ng g c NamaFolder/NamaKomponen
    Contoh : ng generate component ComponentFolder/hero-detail
	Perintah di Atas Akan Menggenerate Component Yang Akan Ditempatkan Di Folder src/app/ComponentFolder/
	Folder Yang Digenerate Namanya hero-detail yang berisi file sebagai berikut
	1. hero-detail.component.css
	2. hero-detail.component.html
	3. hero-detail.component.ts
	4. hero-detail.component.spec.ts

NB:Lihat File penamaan-folder.php
	