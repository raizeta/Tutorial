1.Download MongoDb dari Website Resmi MongoDb,Usahakan Yang 64 Bit
2.Install MongoDb.exe, hasil installan akan berada di folder Program Files\MongoDB
3.Buat Folder Baru,Terserah Mau Dimana,Tapi Usahakan Di Drive C:
4.Contoh C:\>md MongoData\data
5.Di Folder Inilah Akan Disimpan Database Dari MongoDb
6.Masuk Ke Command Prompt dan Arahkan Folder MongoDb yang ada di Program Files\MongoDb\bin
7.Ketik di Command prompt mongod.exe --dbpath=C:\MongoData\data
8.Perintah Diatas Dilakukan supaya MongoDb mengetahui dimana tempat Database akan di Simpan